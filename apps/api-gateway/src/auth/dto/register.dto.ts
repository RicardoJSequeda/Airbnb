import { IsEmail, IsString, MinLength, IsOptional } from 'class-validator';

export class RegisterDto {
  @IsEmail()
  email: string;

  @IsString()
  @MinLength(8, { message: 'La contrase√±a debe tener al menos 8 caracteres' })
  password: string;

  @IsString()
  name: string;

  @IsOptional()
  @IsString()
  avatar?: string;
}
